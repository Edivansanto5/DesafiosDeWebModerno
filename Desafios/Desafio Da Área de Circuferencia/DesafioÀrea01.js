/*  O desafio de hoje é calcular a área de um objeto
    A formula pra se calcular a área e PI x raio²
    pra facilitar voçê a formula vai ser essa PI * raio * raio 
     
*/
const PI = 3.14159
let raio = 10;
let area = PI * (raio * raio)
console.log("A Circunferência da Área de "+ raio +" é " + area + ' m² ')